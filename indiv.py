#!/usr/bin/env python
# -*- coding: utf-8 -*-

# Список для хранения данных
records = []

while True:
    command = input("Введите команду (add, display, exit): ").lower()

    if command == 'exit':
        break
    elif command == 'add':
        record = {}
        record['расчетный счет плательщика'] = input('Введите расчетный счет плательщика: ')
        record['расчетный счет получателя'] = input('Введите расчетный счет получателя: ')
        record['перечисляемая сумма в руб.'] = float(input('Введите перечисляемую сумму в рублях: '))
        records.append(record)
        print('Запись добавлена.')
    elif command == 'display':
        расчетный_счет_плательщика = input('Введите расчетный счет плательщика для поиска суммы: ')
        найден = False

        for record in records:
            if record['расчетный счет плательщика'] == расчетный_счет_плательщика:
                print(f"Сумма, снятая с расчетного счета плательщика {расчетный_счет_плательщика}: {record['перечисляемая сумма в руб.']} руб.")
                найден = True
                break

        if not найден:
            print(f"Расчетный счет плательщика {расчетный_счет_плательщика} не найден.")
    else:
        print("Неверная команда. Попробуйте снова.")

# Сортировка записей по расчетным счетам плательщиков перед выходом
records.sort(key=lambda x: x['расчетный счет плательщика'])